function dayOfWeek(number) {
    if (number == 1) {
        console.log("Понеделник");
    }
    else if (number == 2) {
        console.log("Вторник");
    }
    else if (number == 3) {
        console.log("Сряда");
    }
    else if (number == 4) {
        console.log("Четвъртък");
    }
    else if (number == 5) {
        console.log("Петък");
    }
    else if (number == 6) {
        console.log("Събота");
    }
    else if (number == 7) {
        console.log("Неделя");
    }
    else {
        console.log("ГРЕШКА!");
    }
}

dayOfWeek(5)